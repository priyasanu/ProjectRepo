

public class AddFloatAndIntegerSimple {
    public static void main(String[] args) {
       
        float floatNumber = 12.5f; 
        int intNumber = 8;         

       
        float sum = floatNumber + intNumber;

        // Print the result
        System.out.println("The sum of " + floatNumber + " and " + intNumber + " is: " + sum);
    }
}
